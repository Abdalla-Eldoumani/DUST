/**
 * Get cached page content filtered by difficulty and content type.
 */
export function getCachedPage(
  difficulty: number,
  contentType?: PageContent["contentType"]
): PageContent | null {
  const candidates = CACHED_PAGES.filter((p) => {
    const diffMatch = Math.abs(p.difficulty - difficulty) <= 2;
    const typeMatch = !contentType || p.contentType === contentType;
    return diffMatch && typeMatch;
  });

  if (candidates.length === 0) return CACHED_PAGES[0] ?? null;
  return candidates[Math.floor(Math.random() * candidates.length)] ?? null;
}

/**
 * Get a random cached page that hasn't been used recently.
 */
export function getRandomCachedPage(
  usedIds: string[] = [],
  difficulty?: number
): PageContent {
  let candidates = CACHED_PAGES.filter((p) => !usedIds.includes(p.id));
  if (difficulty !== undefined) {
    const diffFiltered = candidates.filter(
      (p) => Math.abs(p.difficulty - difficulty) <= 2
    );
    if (diffFiltered.length > 0) candidates = diffFiltered;
  }
  if (candidates.length === 0) candidates = CACHED_PAGES;
  return candidates[Math.floor(Math.random() * candidates.length)]!;
}
